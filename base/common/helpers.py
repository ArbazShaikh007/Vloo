def get_localized_service_name(service_obj, lang: str) -> str:
    """Return service name localized per lang, fallback to English."""
    if lang == "ar" and getattr(service_obj, "service_name_ar", None):
        return service_obj.service_name_ar
    if lang == "bn" and getattr(service_obj, "service_name_bn", None):
        return service_obj.service_name_bn
    return service_obj.service_name


def get_notification_message(notif_type: str, lang: str, service: str | None = None):
    data = notification_messages.get(notif_type, {})
    # Fallback to English if the requested lang is missing
    title = data.get("title", {}).get(lang) or data.get("title", {}).get("en", "")
    msg_func = data.get("msg", {}).get(lang) or data.get("msg", {}).get("en")

    msg = msg_func(service) if callable(msg_func) and service is not None else (msg_func if isinstance(msg_func, str) else "")
    return {"title": title, "msg": msg}

